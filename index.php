    <h2>Bio</h2><hr>
    <p>Hi, my name is Arthur Jordan Simonds. You can call me Jordan! I spent my entire childhood in Newtown, Connecticut, and now I live in Troy, New York studying to become a Mechanical Engineer at Rensselaer Polytechnic Institute (better known as RPI). I am a full-time student and an enthusiastic part-time graphic designer. I have a keen sense of style and can make pretty much anything look good. In fact, that is my motto: "Look good!"</p>
    <p>Anyway, I started designing in 3D back in high school using a free program called Blender. I then progressed to print design and made dozens of unique posters for various events, clubs and staff departments around campus here at good ol' RPI. I worked mainly in Adobe Illustrator, with some dabbling in Photoshop and InDesign.</p>
    <p>My passion for digital design grew and grew. Soon I was taking classes and starting my own projects. I really enjoyed designing posters but I wanted to make something more global and timeless. There is nothing better than a logo, I thought, and so I got into branding. I studied hundreds of logos, new and old, trying to appreciate the effort that went into each one. I analyzed their composition, their color, and most importantly how the logo matched the organization. I now have a solid history in branding, habing made logos for clubs, companies, video games, events, and even myself.</p>
    <p>In the spring of 2013, another opportunity arose. The Rensselaer Outing Club, a club I am very active in on campus, lost all of their database information including members, leaders, equipment and officer positions. The old server with the website crashed and only a limited amount of information was recoverable. I promised to completely redesign the website and learn whatever languages and skills I had to in order to get it back to a fully functioning website and database. I taught myself several web languages and how to structure an efficient database.</p>
    <p>By the end of the summer, I had created a beautiful website with all the functionality needed to restock our database of epuipment and members. It had many new features that the old site did not.
    </p>
